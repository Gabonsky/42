#include <unistd.h>

void ft_putchar (char c)
{
	write(1, &c, 1);
}

int main(int *nbr)
	{
		*nbr = 42;
		ft_putchar(*nbr / 10 + '0');
		ft_putchar(*nbr % 10 + '0');
		return (0);
	}
